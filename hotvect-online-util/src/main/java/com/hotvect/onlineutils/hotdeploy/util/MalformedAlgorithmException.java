package com.hotvect.onlineutils.hotdeploy.util;

public class MalformedAlgorithmException extends RuntimeException {
    public MalformedAlgorithmException(String message) {
        super(message);
    }

    public MalformedAlgorithmException(String message, Throwable cause) {
        super(message, cause);
    }

    public MalformedAlgorithmException(Throwable cause) {
        super(cause);
    }
}
